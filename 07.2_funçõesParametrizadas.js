// funções que recebem valores dentro dos parenteses. Esses valores passadas nos parenteses são utilizadas dentro do corpo da função para executar o código. 

function soma(n1, n2) { // o valor passado dentro dos parenteses...
    console.log(n1 + n2) // ...está sendo executado dentro da função
}

soma(34, 45) // chamando a função e passando um valor qualquer que vai ser convertido e utilizado dentro da função, chamamos a função e passamos o valor que será processado para a execução do codigo da função. 
// se a função pede x valores de parametro, precisam ser passado x valores. Se não o resultado é NaN
